export function ImageWithFallback({
  darkMode = true,
  ...props
}: React.ImgHTMLAttributes<HTMLImageElement> & { darkMode?: boolean }) {
  const [didError, setDidError] = useState(false)

  const handleError = () => {
    setDidError(true)
  }

  const { src, alt, style, className, ...rest } = props

  return didError ? (
    <div
      className={`relative inline-block overflow-hidden transition-colors duration-500 ${darkMode ? 'bg-neutral-900' : 'bg-neutral-200'
        } ${className ?? ''}`}
      style={style}
    >
      <div className="absolute inset-0 opacity-20 pointer-events-none mix-blend-overlay"
        style={{ backgroundImage: 'url("data:image/svg+xml,%3Csvg viewBox=\'0 0 200 200\' xmlns=\'http://www.w3.org/2000/svg\'%3E%3Cfilter id=\'noiseFilter\'%3E%3CfeTurbulence type=\'fractalNoise\' baseFrequency=\'0.65\' numOctaves=\'3\' stitchTiles=\'stitch\'/%3E%3C/filter%3E%3Crect width=\'100%25\' height=\'100%25\' filter=\'url(%23noiseFilter)\'/%3E%3C/svg%3E")' }}
      />
      <div className="flex flex-col items-center justify-center w-full h-full gap-2 p-4">
        <svg
          width="24"
          height="24"
          viewBox="0 0 24 24"
          fill="none"
          stroke="currentColor"
          strokeWidth="1"
          strokeLinecap="round"
          strokeLinejoin="round"
          className={darkMode ? 'text-white/20' : 'text-black/20'}
        >
          <rect width="18" height="18" x="3" y="3" rx="2" ry="2" />
          <circle cx="9" cy="9" r="2" />
          <path d="m21 15-3.086-3.086a2 2 0 0 0-2.828 0L6 21" />
        </svg>
        <span className={`text-[10px] tracking-[0.3em] font-medium uppercase ${darkMode ? 'text-white/10' : 'text-black/10'}`}>
          Media Offline
        </span>
      </div>
    </div>
  ) : (
    <img src={src} alt={alt} className={className} style={style} {...rest} onError={handleError} />
  )
}
